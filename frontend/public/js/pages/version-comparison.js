// Screen 9 — Question Version Comparison
import { api } from "../api.js";
import { state } from "../state.js";
import {
  el, clear, pageHead, card, badge, button, alert, icon, toast,
} from "../components.js";

export async function render({ navigate }) {
  const token = state.get("reviewToken");
  const root = el("div", { class: "page" });
  root.appendChild(pageHead("Review Proposed Change", "Compare the original and regenerated question before making a final decision."));

  if (!token) {
    root.appendChild(alert("Open the SME review workspace from the secure link first.", "info"));
    root.appendChild(button("Go to Review Queue", { variant: "primary", onClick: () => navigate("#/review") }));
    return root;
  }

  // Prefer the version produced in the rework step; otherwise fall back to the
  // latest version on the selected question.
  let version = state.get("lastVersion");
  if (!version) {
    try {
      const session = await api.getReviewSession(token);
      const q = (session.questions || []).find((x) => x.question_id === state.get("selectedQuestionId"));
      const versions = q && q.versions ? q.versions : [];
      version = versions[versions.length - 1];
    } catch (e) { root.appendChild(alert(e.message, "error")); return root; }
  }

  if (!version) {
    root.appendChild(alert("No regenerated version to compare yet. Regenerate a question first.", "info"));
    root.appendChild(button("Go to Rework", { variant: "primary", onClick: () => navigate("#/regenerate") }));
    return root;
  }

  const orig = el("div", { class: "card", style: "border-width:2px" },
    el("div", { class: "card-head", style: "background:var(--surface)" },
      el("div", { class: "flex items-center justify-between" }, el("div", { class: "card-title" }, "Original Version"), badge("Rejected", "outline-red"))),
    el("div", { class: "card-body flex-col gap-4" },
      kv("Question", version.old_question),
      kv("Answer", version.old_answer)));

  const neu = el("div", { class: "card", style: "border:2px solid var(--genpal-purple)" },
    el("div", { class: "card-head", style: "background:rgba(161,0,255,0.05)" },
      el("div", { class: "flex items-center justify-between" }, el("div", { class: "card-title" }, "New Proposed Version"), badge("Regenerated", "purple"))),
    el("div", { class: "card-body flex-col gap-4" },
      kv("Question", version.new_question),
      kv("Answer", version.new_answer),
      el("div", { class: "flex-col gap-2" },
        kvRow("Documentation Check", badge("Checked", "success")),
        kvRow("Duplicate Check", badge("Passed", "success")),
        kvRow("Change Status", badge(version.change_status || "Pending", "outline")))));

  root.appendChild(el("div", { class: "grid grid-2" }, orig, neu));

  const decisionHost = el("div", {});
  function renderDecision() {
    clear(decisionHost).appendChild(card({
      title: "Decision",
      body: el("div", { class: "flex gap-3 flex-wrap" },
        button("Accept Change", { variant: "success", iconName: "check", onClick: accept }),
        button("Reject Change", { variant: "danger", iconName: "x", onClick: reject }),
        button("Regenerate Again", { variant: "outline", iconName: "refresh", onClick: () => navigate("#/regenerate") })),
    }));
  }
  async function accept() {
    try { await api.acceptVersion(token, version.version_id); state.set({ lastVersion: null }); clear(decisionHost).appendChild(alert("Change accepted. Question updated to the regenerated version.", "success", "checkCircle")); toast("Change accepted.", "success"); setTimeout(() => navigate("#/review"), 1200); }
    catch (e) { toast(e.message, "error"); }
  }
  async function reject() {
    try { await api.rejectVersion(token, version.version_id); state.set({ lastVersion: null }); clear(decisionHost).appendChild(alert("Change rejected. Original version remains active.", "error")); toast("Change rejected.", "info"); setTimeout(() => navigate("#/sme-question"), 1200); }
    catch (e) { toast(e.message, "error"); }
  }
  renderDecision();
  root.appendChild(decisionHost);

  // Audit timeline (derived)
  const steps = [
    ["Generated", "Initial question generated by AI.", true],
    ["SME Feedback / Rejection", version.sme_feedback || "SME requested rework.", true],
    ["Regenerated with Feedback", "New version produced from SME feedback.", true],
    ["SME Decision", "Awaiting your accept / reject.", false],
  ];
  const timeline = el("div", { class: "flex-col" });
  steps.forEach(([label, detail, done], i) => {
    timeline.appendChild(el("div", { class: "flex gap-4" },
      el("div", { class: "flex flex-col items-center" },
        el("div", { style: `width:32px;height:32px;border-radius:50%;display:flex;align-items:center;justify-content:center;border:2px solid ${done ? "var(--genpal-purple)" : "var(--border)"};background:${done ? "var(--genpal-purple)" : "#fff"}` },
          done ? icon("check", "ico") : el("span", { style: "width:8px;height:8px;border-radius:50%;background:var(--border)" })),
        i < steps.length - 1 ? el("div", { style: "width:2px;height:30px;background:var(--border);margin:4px 0" }) : null),
      el("div", { style: "padding-bottom:1rem" },
        el("p", { class: `t-sm ${done ? "t-dark" : "t-muted"}` }, label),
        el("p", { class: "t-xs t-muted" }, detail))));
  });
  root.appendChild(card({ title: "Audit Timeline", body: timeline }));

  return root;
}

function kv(k, v) {
  return el("div", {}, el("p", { class: "t-xs t-muted" }, k), el("p", { class: "t-xs t-dark mt-1" }, v || "—"));
}
function kvRow(k, node) {
  return el("div", { class: "flex justify-between items-center t-xs" }, el("span", { class: "t-muted" }, k), node);
}
